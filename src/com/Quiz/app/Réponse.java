package com.Quiz.app;

public class Réponse {
	
	
	private int num;
	private String Desc;
	
	public Réponse(int num,String Desc){
		this.num = num;
		this.Desc = Desc;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getDesc() {
		return Desc;
	}

	public void setDesc(String desc) {
		Desc = desc;
	}
	
	
	
	

}
